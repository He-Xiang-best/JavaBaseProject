package asia.china;
/**
 *生成一个类Employee,
 *域变量为name和age;类包为asia.china;
 * @author 郭晓燕
 * @since JDK1.6
 * @version 1.0
 * @since 2017.3.30
 */
public class Employee {
	protected String name;
	protected int age;
	
	public String getName() {
		return name;
	}
	public void setName(String name) {
		this.name = name;
	}
	public int getAge() {
		return age;
	}
	public void setAge(int age) {
		this.age = age;
	}
	
}
